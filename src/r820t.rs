const INIT: [u8; 27] = [
    0x83, 0x32, 0x75, 0xc0, 0x40, 0xd6, 0x6c, 0xf5, 0x63, 0x75, 0x68, 0x6c, 0x83, 0x80, 0x00, 0x0f,
    0x00, 0xc0, 0x30, 0x48, 0xcc, 0x60, 0x00, 0x54, 0xae, 0x4a, 0xc0,
];

pub struct R820T {
    shadow: [u8; 27],
}

impl R820T {
    pub fn read(addr: u8) {
        // hardware always starts at reg 0; ask for N+1 bytes
        let count = u16::from(addr) + 1;
        let bytes = i2c.read(0x34, 0x00, count)?;
        let value = bytes[addr as usize].reverse_bits();
    }
}
